//Bucle while

//Ejemplo 1
let i = 0;
let max = 10;
//Se establece una condicion y miestras esta se cumpla el bucle se ejecuta
while (i < max) {
    console.log(i);
    i++
}

console.log("-----------------------------------------");


//Bucle do while

/*El bloque de código dentro del do se ejecuta siempre al menos una vez,
 sin importar si la condición es verdadera o falsa en la primera evaluación.
*/
let j = 0;
let maxi = 5;

do {
    console.log(i);
    j++;
} while (j < maxi);
